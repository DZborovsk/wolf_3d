#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		l;

	i = 0;
	l = 0;
	while (s1[l])
		l++;
	while (s2[i] && n > 0)
	{
		s1[l] = s2[i];
		i++;
		n--;
		l++;
	}
	s1[l] = '\0';
	return (s1);
}
