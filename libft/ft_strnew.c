#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char*)malloc(sizeof(char) * (size + 1));
	if (str == NULL)
		return ((void*)0);
	ft_bzero(str, size + 1);
	return (str);
}
